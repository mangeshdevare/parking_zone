$(document).ready(function () {
  $('#exampleModal').on('show.bs.modal', function (event) {
    var button = $(event.relatedTarget) // Button that triggered the modal
    var recipient = button.data('whatever') // Extract info from data-* attributes
    var intime = button.data('intime') // Get Vehicle's out time
    var hour_diff = Math.ceil(( new Date($.now()) - new Date(intime) ) / 1000 / 60 / 60)
    var hourlycharge = button.data('hourlycharge')
    var total_charges = hour_diff * hourlycharge
    // If necessary, you could initiate an AJAX request here (and then do the updating in a callback).
    // Update the modal's content. We'll use jQuery here, but you could use a data binding library or other methods instead.
    var modal = $(this)
    modal.find('.modal-title').text('New message to ' + recipient)
    modal.find('.modal-body input#registration_number').val(recipient)
    modal.find('.modal-body input#total_charges').val(total_charges)
  })  
});